const uint8_t* FONT_Get(uint8_t id) {
    switch (id) {
        case 0x00: return font_6x8;
        case 0x01: return font_8x8;
        case 0x02: return font_10x16;
        case 0x03: return font_12x16;
        case 0x04: return img_16x32;
        case 0x05: return img_16x8;
        case 0x06: return img_128x32;
        default: return font_6x8;
    }
}

void FONT_Size(const uint8_t* font, uint8_t* w, uint8_t* h) {
    uint8_t f_o, f_e;
    FONT_Info(font, w, h, &f_o, &f_e);
}

void FONT_Info(const uint8_t* font, uint8_t* w, uint8_t* h, uint8_t* offset, uint8_t* end) {
    if (font == font_6x8) {
        *w = 6; *h = 8; *offset = ' '; *end = 0x7F;
    } else if (font == font_8x8) {
        *w = 8; *h = 8; *offset = ' '; *end = 0x7F;
    } else if (font == font_10x16) {
        *w = 10; *h = 16; *offset = ' '; *end = 0x7F;
    } else if (font == font_12x16) {
        *w = 12; *h = 16; *offset = ' '; *end = '?';
    } else if (font == img_16x32) {
        *w = 16; *h = 32; *offset = 0x00; *end = 0x04;
    } else if (font == img_16x8) {
        *w = 16; *h = 8; *offset = 0x00; *end = 0x03;
    } else if (font == img_128x32) {
        *w = 128; *h = 32; *offset = 0x00; *end = 0x00;
    }
}
